let currentLang = localStorage.getItem('vpa_lang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('vpa_lang', lang);

    // Update UI elements with data-key
    document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.getAttribute('data-key');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });

    // Update active state on language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.innerText.toLowerCase() === lang || (btn.innerText === 'KH' && lang === 'kh'));
    });

    // Update font based on language
    if (lang === 'kh') {
        document.body.style.fontFamily = "'Khmer OS Battambang', 'Outfit', sans-serif";
    } else {
        document.body.style.fontFamily = "'Outfit', 'Khmer OS Battambang', sans-serif";
    }
}

