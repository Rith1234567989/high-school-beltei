function openAdmissionModal(stepId) {
    const data = admissionDetails[stepId];
    if (!data) return;

    const langData = data[currentLang];
    const modal = document.getElementById('subjectModal');
    const title = document.getElementById('modalTitle');
    const list = document.getElementById('modalDetails');
    const subtitle = modal.querySelector('p');

    title.innerText = langData.title;
    list.innerHTML = langData.details.map(item => `<li>${item}</li>`).join('');

    if (subtitle) {
        subtitle.innerText = currentLang === 'kh' ? 'á–áŸááŸŒá˜á¶á“á›á˜áŸ’á¢á·á (Details):' : 'Additional Step Details:';
    }

    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function showGrade(gradeId) {
    const container = document.getElementById('grade-content');
    if (!container) return;

    // Update active tab button
    document.querySelectorAll('.tab-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('onclick') && btn.getAttribute('onclick').includes(gradeId)) {
            btn.classList.add('active');
        }
    });

    const data = gradeSubjects[gradeId];
    let html = '';

    if (data.subjects) {
        // Grade 10 (Single list)
        html = data.subjects.map((s, idx) => `
            <div class="subject-item clickable" onclick="openSubjectModal('${gradeId}', ${idx})">
                <div class="subject-main">
                    <span class="subject-icon">${s.icon}</span>
                    <span class="subject-name">${s.name}</span>
                </div>
                <p class="subject-details">${s.info || 'Essential curriculum topics'}</p>
                <span class="click-hint">${currentLang === 'kh' ? 'á…á»á…áŸá˜áŸ’ášá¶á”áŸ‹á–áŸááŸŒá˜á¶á“á›á˜áŸ’á¢á·á â†’' : 'Click for details â†’'}</span>
            </div>
        `).join('');
    } else if (data.streams) {
        // Grade 11 & 12 (Two streams)
        html = `<div style="grid-column: 1 / -1; display: grid; grid-template-columns: repeat(auto-fit, minmax(400px, 1fr)); gap: 40px;">`;
        data.streams.forEach((stream, sIdx) => {
            html += `
                <div class="stream-box card" style="background: white;">
                    <h3 style="display: flex; align-items: center; gap: 10px; margin-bottom: 25px; border-bottom: 2px solid var(--primary); padding-bottom: 15px;">
                        <span>${stream.icon}</span> ${stream.name}
                    </h3>
                    <div style="display: grid; grid-template-columns: 1fr 1fr; gap: 15px;">
                        ${stream.subjects.map((s, idx) => `
                            <div class="subject-item small ${s.details ? 'clickable' : ''}"
                                 ${s.details ? `onclick="openSubjectModal('${gradeId}', ${idx}, ${sIdx})"` : ''}
                                 style="padding: 10px; flex-direction: row; align-items: center; position: relative;">
                                <span class="subject-icon" style="font-size: 1.2rem;">${s.icon}</span>
                                <span class="subject-name" style="font-size: 0.9rem;">${s.name}</span>
                                ${s.details ? '<span style="position: absolute; right: 5px; color: var(--accent); font-size: 0.7rem;">+</span>' : ''}
                            </div>
                        `).join('')}
                    </div>
                </div>
            `;
        });
        html += `</div>`;
    }

    container.innerHTML = html;
}

function openSubjectModal(gradeId, subjectIdx, streamIdx = null) {
    let subject;
    if (streamIdx !== null) {
        subject = gradeSubjects[gradeId].streams[streamIdx].subjects[subjectIdx];
    } else {
        subject = gradeSubjects[gradeId].subjects[subjectIdx];
    }

    if (!subject || !subject.details) return;

    const modal = document.getElementById('subjectModal');
    const title = document.getElementById('modalTitle');
    const list = document.getElementById('modalDetails');
    const subtitle = modal.querySelector('p');

    title.innerHTML = `${subject.icon} ${subject.name}`;
    list.innerHTML = subject.details.map(item => `<li>${item}</li>`).join('');

    if (subtitle) {
        subtitle.innerText = currentLang === 'kh' ? 'á¢áŸ’áœá¸áŠáŸ‚á›á¢áŸ’á“á€á“á¹á„áŸá·á€áŸ’áŸá¶ (ášáŸ€á“á¢áŸ†á–á¸):' : 'What you will learn (ášáŸ€á“á¢áŸ†á–á¸):';
    }

    modal.classList.add('active');
    document.body.style.overflow = 'hidden'; // Prevent scroll
}

function closeModal() {
    const modal = document.getElementById('subjectModal');
    if (modal) {
        modal.classList.remove('active');
        document.body.style.overflow = 'auto';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const nav = document.getElementById('navbar');

    // Initialize language
    setLanguage(currentLang);

    // Navbar scroll effect
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            nav.classList.add('scrolled');
        } else {
            nav.classList.remove('scrolled');
        }
    });

    // Initial render for academics page if it exists
    if (document.getElementById('grade-content')) {
        showGrade('grade10');
    }

    // Modal Close behavior
    const modal = document.getElementById('subjectModal');
    if (modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) closeModal();
        });
    }

    // Language Switcher Logic
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.innerText.toLowerCase() === 'kh' ? 'kh' : 'en';
            setLanguage(lang);

            // Re-render grade content if on academics page
            if (document.getElementById('grade-content')) {
                const activeTab = document.querySelector('.tab-btn.active');
                if (activeTab) {
                    const gradeId = activeTab.getAttribute('onclick').match(/'([^']+)'/)[1];
                    showGrade(gradeId);
                }
            }
        });
    });

    // Form submission simulation
    const forms = document.querySelectorAll('form');
    forms.forEach(form => {
        form.addEventListener('submit', (e) => {
            e.preventDefault();
            const btn = form.querySelector('button');
            const originalText = btn.innerText;

            btn.innerText = currentLang === 'kh' ? 'á€áŸ†á–á»á„á”á‰áŸ’á‡á¼á“...' : 'Processing...';
            btn.disabled = true;

            setTimeout(() => {
                const successMsg = currentLang === 'kh'
                    ? 'á€á¶ášá”á‰áŸ’á‡á¼á“á‘á‘á½á›á”á¶á“á‡áŸ„á‚á‡áŸá™! áŸá¼á˜á¢ášá‚á»ááŸá˜áŸ’ášá¶á”áŸ‹á€á¶ášá‘á¶á€áŸ‹á‘á„á˜á€áœá·á‘áŸ’á™á¶á›áŸá™ áœáŒáŸ’áá“áŸˆáŸ”'
                    : 'Submission successful! Thank you for contacting Vattanak Premier Academy.';
                alert(successMsg);
                btn.innerText = originalText;
                btn.disabled = false;
                form.reset();
            }, 1000);
        });
    });

    // Reveal animation observer with improved settings
    const observerOptions = {
        threshold: 0.15,
        rootMargin: '0px 0px -100px 0px'
    };

    const revealObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('revealed');
                revealObserver.unobserve(entry.target); // Only animate once
            }
        });
    }, observerOptions);

    // Apply to sections and cards
    const revealElements = document.querySelectorAll('section, .card, .subject-item');
    revealElements.forEach((el, index) => {
        el.classList.add('reveal-init');
        el.style.transitionDelay = `${(index % 3) * 0.1}s`; // Stagger effect
        revealObserver.observe(el);
    });
});
